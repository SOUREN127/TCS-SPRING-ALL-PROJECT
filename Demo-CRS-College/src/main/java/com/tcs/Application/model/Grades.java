package com.tcs.Application.model;

public class Grades {
	
	private double grade;

	public Grades(double grade) {
		super();
		this.grade = grade;
	}
	
	public Grades()
	{
		
	}

	/**
	 * @return the grade
	 */
	public double getGrade() {
		return grade;
	}

	/**
	 * @param grade the grade to set
	 */
	public void setGrade(double grade) {
		this.grade = grade;
	}
	
	

}